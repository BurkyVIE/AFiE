# TRANSFORM ----
## Teamdata ----
teamdata <-
  teamdata |> mutate(across(Team:Home, ~ modify(.x, ~ unnest_longer(., "Season")))) # expandiere Seasons in nested tibbles

# RESPONSE ----
cat("\033[1;36m..AFiE >\033[0m object \033[33mteamdata\033[0m imported \033[1;92m✔\033[0m\n")

## Team_info ----
teaminfo <-
  select(teamdata, Franchise, Team) |> unnest_longer(Team) |> unpack(Team) |>                   # extrahiere TEAM Info
  left_join(
    select(teamdata, Franchise, League) |> unnest_longer(League) |> unpack(League),             # extrahiere LEAGUE Info
    by = c("Franchise", "Season")) |>
  left_join(
    select(teamdata, Franchise, Conference) |> unnest_longer(Conference) |> unpack(Conference), # extrahiere CONFERENCE Info
    by = c("Franchise", "Season")) |>
  left_join(
    select(teamdata, Franchise, Division) |> unnest_longer(Division) |> unpack(Division),       # extrahiere DIVISION Info
    by = c("Franchise", "Season"))

# RESPONSE ----
cat("\033[1;36m..AFiE >\033[0m  ⤷ object \033[33mteaminfo\033[0m generated \033[1;92m✔\033[0m\n")

## Team_location ----
teamloc <- 
  select(teamdata, Franchise, Home) |> unnest_longer(Home) |> unpack(Home) |> # extrahiere HOME Info
  left_join(
    teaminfo |> select(Season, League, Franchise, Conference, Division),
    by = c("Franchise", "Season"))

